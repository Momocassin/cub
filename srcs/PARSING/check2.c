bool	check_color(int r, int g, int b)
{
	if ((r < 0 || g < 0 || b < 0) || (r > 255 || g > 255 || b > 255))
	{
		printf("Error : 0 <= RGB <= 255\n");
		return (1);
	}
	return (0);
}
